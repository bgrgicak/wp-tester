import type { Environment, WPTesterConfig } from "@wp-tester/config";
import { resolveConfig, getProjectDir, findPhpUnitConfig, parseBootstrapPath, getProjectRootMount } from "@wp-tester/config";
import type { Report } from "@wp-tester/results";
import { EMPTY_REPORT, mergeReports } from "@wp-tester/results";
import { startPlayground } from "@wp-tester/runtime";
import { parseJUnitXml } from "./junit-parser";


export function shouldRunPhpUnitTests(config: WPTesterConfig): boolean {
  return config.tests?.phpunit !== undefined;
}

/**
 * Run PHPUnit tests in WordPress Playground environment for a specific environment
 *
 * @param config - Resolved test configuration
 * @param environment - Environment configuration
 * @param projectRoot - Project root directory
 * @param hostPhpunitConfigPath - Path to PHPUnit config file
 * @returns CTRF report with test results
 */
async function runPhpUnitTestsForEnvironment(
  config: WPTesterConfig,
  environment: Environment,
  projectRoot: string,
  hostPhpunitConfigPath: string
): Promise<Report> {
  const runtime = await startPlayground(environment);
  const playground = runtime.playground;

  if (config.reporters && config.reporters.includes("default")) {
    console.log(`Running PHPUnit tests for environment: ${environment.name}`);
  }

  try {
    // Determine the plugin or theme being tested
    if (!config.projectType) {
      console.error("Project type must be specified in config for PHPUnit tests");
      return EMPTY_REPORT;
    }

    const mount = getProjectRootMount(projectRoot, config.projectType);
    if (!mount) {
      console.error(`Project type '${config.projectType}' does not support PHPUnit tests`);
      return EMPTY_REPORT;
    }
    const projectVfsPath = mount.vfsPath;

    // Parse bootstrap path from PHPUnit config on the host filesystem
    const bootstrapPath = await parseBootstrapPath(hostPhpunitConfigPath);
    // Determine bootstrap file path in the playground filesystem
    let userBootstrap = `${projectVfsPath}/tests/bootstrap.php`; // Default fallback
    if (bootstrapPath) {
      userBootstrap = `${projectVfsPath}/${bootstrapPath}`;
    }

    // Create a custom bootstrap file that loads WordPress, then user's bootstrap
    let bootstrapFilePath: string | null = null;
    if (projectVfsPath) {
      bootstrapFilePath = `/tmp/wp-tester-bootstrap.php`;

      const bootstrapContent = `<?php
// Auto-generated by wp-tester - loads WordPress then user's bootstrap

// 1. Load WordPress
if (file_exists('/wordpress/wp-load.php')) {
  require_once '/wordpress/wp-load.php';
}

// 2. Load user's bootstrap file (where they can load their plugin/theme)
if (file_exists('${userBootstrap}')) {
  require_once '${userBootstrap}';
}
`;
      await playground.writeFile(bootstrapFilePath, bootstrapContent);
    }

    // npx @php-wasm/cli@1.1.2 ./vendor/bin/phpunit
    const logFilePath = `/tmp/phpunit-results-${Date.now()}.xml`;

    // Build PHP CLI arguments
    const cliArgs = [
      "php",
      `${projectVfsPath}/vendor/bin/phpunit`,
      "-c",
      `${projectVfsPath}/phpunit.xml.dist`,
      "--log-junit",
      logFilePath
    ];

    // Override bootstrap file if we created a custom one
    if (bootstrapFilePath) {
      cliArgs.push("--bootstrap", bootstrapFilePath);
    }

    // Run PHPUnit tests using WP-CLI
    const result = await playground.cli(cliArgs);

    if (!config.reporters || config.reporters.includes("default")) {
      await result.stdout.pipeTo(new WritableStream({
        write(chunk) {
          process.stdout.write(chunk);
        }
      }));
    } else {
      // Still need to consume the stream even if not displaying
      await result.stdout.cancel();
    }

    const exitCode = await result.exitCode;

    if (exitCode !== 0 && exitCode !== 1) {
      // Exit code 1 indicates test failures, which is acceptable
      console.error(`PHPUnit exited with code ${exitCode}`);
      return EMPTY_REPORT;
    }

    // Check if the log file was created
    if (!await playground.fileExists(logFilePath)) {
      console.error(`PHPUnit log file not created at ${logFilePath}`);
      return EMPTY_REPORT;
    }

    // Read the JUnit XML log file
    const xmlString = await playground.readFileAsText(logFilePath);

    // Parse the JUnit XML output to CTRF format
    const environmentName = environment.name;
    const report = await parseJUnitXml(xmlString, environmentName);

    return report;
  } catch (error) {
    console.error(`Error running PHPUnit tests for environment "${environment.name}":`, error);
    return EMPTY_REPORT;
  } finally {
    // Cleanup
    runtime.server.close();
  }
}

/**
 * Run PHPUnit tests in WordPress Playground environment
 *
 * @param config - Test configuration or path to config file
 * @returns CTRF report with test results
 */
export async function runPhpUnitTests(
  config: WPTesterConfig | string
): Promise<Report> {
  // Resolve config (loads from path if string, resolves paths)
  const resolvedConfig = await resolveConfig(config);

  // Check if PHPUnit tests are configured
  if (!shouldRunPhpUnitTests(resolvedConfig)) {
    return Promise.resolve(EMPTY_REPORT);
  }

  // Get the project root directory from the config
  const configPath = typeof config === "string" ? config : undefined;
  const projectRoot = getProjectDir(resolvedConfig, configPath);

  // Find PHPUnit config in the project root
  const hostPhpunitConfigPath = await findPhpUnitConfig(projectRoot);
  if (!hostPhpunitConfigPath) {
    console.error("PHPUnit config file not found in project root");
    return Promise.resolve(EMPTY_REPORT);
  }

  // Run tests for all environments
  const reports: Report[] = [];
  for (const environment of resolvedConfig.environments) {
    const report = await runPhpUnitTestsForEnvironment(
      resolvedConfig,
      environment,
      projectRoot,
      hostPhpunitConfigPath
    );
    if (report.results.summary.tests > 0) {
      reports.push(report);
    }
  }

  // Return empty report if no tests ran
  if (reports.length === 0) {
    return EMPTY_REPORT;
  }

  // Return single report as-is, or merge multiple reports
  if (reports.length === 1) {
    return reports[0];
  }

  // Import mergeReports for multiple environments
  return mergeReports(reports);
}
