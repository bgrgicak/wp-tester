import type {
  WPTesterConfig,
  ResolvedWPTesterConfig,
  ResolvedEnvironment,
} from "@wp-tester/config";
import {
  parseBootstrapPath,
  hostToVfs,
  resolveAbsolute,
} from "@wp-tester/config";
import { defineWpConfigConsts } from "@wp-playground/blueprints";
import type { Report } from "@wp-tester/results";
import { EMPTY_REPORT, mergeReports, PHPUnitStreamingReporter, TeamCityParser } from "@wp-tester/results";
import { startPlayground } from "@wp-tester/runtime";
import { mountWordPressTestLibrary } from "./wordpress-test-lib";
import { access } from "fs/promises";

export function shouldRunPhpunitTests(config: WPTesterConfig): boolean {
  return config.tests?.phpunit !== undefined;
}

/**
 * Run PHPUnit tests in WordPress Playground environment for a specific environment
 *
 * @param config - Resolved test configuration
 * @param environment - Environment configuration
 * @param hostPhpunitConfigPath - Absolute path to PHPUnit config on host
 * @returns CTRF report with test results
 */
async function runPhpunitTestsForEnvironment(
  config: ResolvedWPTesterConfig,
  environment: ResolvedEnvironment,
  hostPhpunitConfigPath: string,
): Promise<Report> {
  const testMode = config.tests.phpunit!.testMode;
  let environmentWithMount: ResolvedEnvironment = {
    ...environment,
  };

  // Prepare environment with WordPress test library for unit tests only
  // Integration tests use standard WordPress installation via wp-load.php
  if (testMode === "unit") {
    environmentWithMount = await mountWordPressTestLibrary(environment);
  }

  // Start playground with all mounts and steps including test library initialization
  const runtime = await startPlayground(environmentWithMount);
  const playground = runtime.playground;

  // Set WP_TESTS_DOMAIN constant
  await defineWpConfigConsts(playground, {
    consts: {
      WP_TESTS_DOMAIN: new URL(await playground.absoluteUrl).host,
    },
  });

  try {
    // Determine the plugin or theme being tested
    if (!config.projectType) {
      console.error(
        "Project type must be specified in config for PHPUnit tests",
      );
      return EMPTY_REPORT;
    }

    if (!config.projectVFSPath) {
      console.error(
        `Project type '${config.projectType}' does not support PHPUnit tests`,
      );
      return EMPTY_REPORT;
    }

    // Map host PHPUnit config path to VFS
    const vfsPhpunitConfigPath = hostToVfs(hostPhpunitConfigPath, config);

    // Map host PHPUnit executable path to VFS
    const vfsPhpunitPath = hostToVfs(config.tests.phpunit!.phpunitPath, config);

    // Only create WordPress bootstrap in integration mode
    let bootstrapFilePath: string | null = null;
    if (testMode === "integration") {
      // Parse bootstrap path from PHPUnit config and map to VFS
      const bootstrapPath = await parseBootstrapPath(hostPhpunitConfigPath);
      // Determine bootstrap file path in the playground filesystem
      let userBootstrap = `${config.projectVFSPath}/tests/bootstrap.php`; // Default fallback
      if (bootstrapPath) {
        // Bootstrap path from parseBootstrapPath is relative, convert to absolute
        const absoluteBootstrapPath = resolveAbsolute(
          bootstrapPath,
          config.projectHostPath,
        );
        userBootstrap = hostToVfs(absoluteBootstrapPath, config);
      }

      // Create a custom bootstrap file that loads WordPress, then user's bootstrap if it exists
      bootstrapFilePath = `/tmp/wp-tester-bootstrap.php`;
      await playground.writeFile(
        bootstrapFilePath,
        `<?php
      // Auto-generated by wp-tester - loads WordPress then user's bootstrap

      // 1. Load WordPress
      if (file_exists('/wordpress/wp-load.php')) {
        require_once '/wordpress/wp-load.php';
      }
      // 2. Load user's bootstrap file if it exists
      if (!empty('${userBootstrap}') && file_exists('${userBootstrap}')) {
        require_once '${userBootstrap}';
      }
      `,
      );
    }

    // Build PHP CLI arguments with environment variables
    // Use TeamCity format for streaming output
    const cliArgs = [
      "php",
      // Set variables_order to EGPCS to ensure environment variables are accessible.
      // This is required for WordPress test library to access WP_TESTS_DIR via getenv().
      // Default PHP settings often exclude 'E' (Environment), which would break test setup.
      "-d",
      "variables_order=EGPCS",
      vfsPhpunitPath,
      "-c",
      vfsPhpunitConfigPath,
      "--teamcity", // Use TeamCity format for streaming
    ];

    // Override bootstrap file with our custom one (only in integration mode)
    if (bootstrapFilePath !== null) {
      cliArgs.push("--bootstrap", bootstrapFilePath);
    }

    // Append additional PHPUnit arguments from config
    // Resolve file/directory paths from host to VFS
    const phpunitArgs = config.tests.phpunit!.phpunitArgs;
    if (phpunitArgs && phpunitArgs.length > 0) {
      // Use the helper to resolve arguments
      const resolvedArgs = await resolvePhpunitArgs(
        phpunitArgs,
        config,
        playground,
      );

      cliArgs.push(...resolvedArgs);
    }

    // Create streaming reporter
    // Disable summary since the CLI will print a combined summary
    const useStreaming = config.reporters?.default !== undefined;

    // Get filter options from config reporters (only if it's an object, not boolean)
    const filter =
      typeof config.reporters?.default === "object"
        ? config.reporters.default
        : undefined;

    const reporter = new PHPUnitStreamingReporter({
      enabled: useStreaming,
      showSummary: false,
      filter,
    });

    // Signal test run start to initialize timing
    reporter.onEvent({
      type: "run:start",
      toolName: "wp-tester-phpunit",
    });

    reporter.onEvent({
      type: "suite:start",
      name: `${environment.name} - PHPUnit ${testMode} tests`,
    });

    // Create TeamCity parser connected to the reporter
    const parser = TeamCityParser.withReporter(reporter);

    // Run PHPUnit tests
    const result = await playground.cli(cliArgs, {
      env: environmentWithMount.env,
    });

    // Process stdout and stderr with TeamCity parser for streaming results
    // PHPUnit outputs TeamCity format to both stdout and stderr
    const textDecoder = new TextDecoder();
    const stderrDecoder = new TextDecoder();
    let stdoutCapture = "";
    let stderrCapture = "";
    await Promise.all([
      result.stdout.pipeTo(
        new WritableStream({
          write(chunk) {
            const text = textDecoder.decode(chunk, { stream: true });
            stdoutCapture += text;
            parser.write(text);
          },
          close() {
            parser.flush();
          },
        }),
      ),
      result.stderr.pipeTo(
        new WritableStream({
          write(chunk) {
            const text = stderrDecoder.decode(chunk, { stream: true });
            stderrCapture += text;
            // Try to parse as TeamCity format first
            parser.write(text);

            // If streaming is disabled, also write non-TeamCity lines to stderr
            // This ensures error messages still appear
            if (!useStreaming) {
              const lines = text.split("\n");
              for (const line of lines) {
                if (!line.trim().startsWith("##teamcity[")) {
                  process.stderr.write(line + "\n");
                }
              }
            }
          },
          close() {
            parser.flush();
          },
        }),
      ),
    ]);

    // Close the outer suite that was started earlier
    reporter.onEvent({
      type: "suite:end",
      name: `${environment.name} - PHPUnit ${testMode} tests`,
    });

    // Signal test run end to finalize timing
    reporter.onEvent({
      type: "run:end",
    });

    const exitCode = await result.exitCode;

    // Get the report from the streaming reporter
    const report = reporter.getReport();

    // PHPUnit outputs errors to stdout, not stderr, so we need to check both
    const errorOutput = stderrCapture.trim() || stdoutCapture.trim();

    // Check for bad exit codes that indicate fatal errors
    // Exit code 0 = success, 1 = test failures, 2 = errors (all acceptable)
    // Other exit codes (e.g., 127 = command not found, 255 = fatal error) are not acceptable
    if (exitCode !== 0 && exitCode !== 1 && exitCode !== 2) {
      // For bad exit codes without error output or with tests that already ran, return empty report
      if (!errorOutput || report.results.tests.length > 0) {
        console.error("\nPHPUnit tests could not run.");

        // Provide specific guidance based on common exit codes
        if (exitCode === 127) {
          console.error("The PHPUnit executable was not found.");
          console.error("Check that:");
          console.error("  - PHPUnit is installed (run: composer install)");
          console.error(
            "  - The phpunitPath in your config points to the correct location",
          );
        } else if (exitCode === 255) {
          console.error("PHPUnit encountered a fatal error.");
          console.error(
            "Check your phpunit.xml configuration and test bootstrap files.",
          );
        } else {
          console.error(`Unexpected error occurred (exit code: ${exitCode})`);
          console.error(
            "Check your PHPUnit configuration and ensure all dependencies are installed.",
          );
        }

        return EMPTY_REPORT;
      }
      // Fall through to create synthetic test below
    }

    // Add error output to CTRF results if present and no tests ran
    // This handles both bad exit codes (from above) and acceptable exit codes with errors
    if (errorOutput && report.results.tests.length === 0) {
      // Check if this is a "no tests executed" scenario vs an actual error
      // PHPUnit outputs "No tests executed!" when no tests match the filter
      const noTestsPattern = /No tests executed!?/i;
      const isNoTestsExecuted =
        noTestsPattern.test(errorOutput) && exitCode === 0;

      if (false === isNoTestsExecuted) {
        // Bootstrap failure - create a synthetic test with the error
        report.results.tests.push({
          name: "PHPUnit Bootstrap",
          status: "failed",
          duration: 0,
          message: "Bootstrap failed - see trace for details",
          trace: errorOutput,
        });
        report.results.summary.tests = 1;
        report.results.summary.failed = 1;
      }
      // If no tests executed (isNoTestsExecuted === true), return empty report
      // The runner will handle this based on passWithNoTests option
    } else if (stderrCapture.trim()) {
      // Tests ran but there's stderr - add it to extra field
      report.results.extra = {
        ...report.results.extra,
        stderr: stderrCapture.split("\n"),
      };
    }

    // Update tool name with environment
    report.results.tool.name = `wp-tester-phpunit (${environment.name})`;

    return report;
  } catch (error) {
    console.error(
      `Error running PHPUnit tests for environment "${environment.name}":`,
      error,
    );
    return EMPTY_REPORT;
  } finally {
    // Cleanup - ensure server closes even on errors
    try {
      await new Promise<void>((resolve) => {
        runtime.server.close(() => resolve());
      });
    } catch (error) {
      console.error("Error closing server:", error);
    }
  }
}

/**
 * Options for running PHPUnit tests
 */
export interface RunPhpunitTestsOptions {
  /** Additional PHPUnit arguments to append */
  phpunitArgs?: string[];
}

/**
 * Run PHPUnit tests in WordPress Playground environment
 *
 * @param config - Resolved test configuration
 * @param phpunitArgsOrOptions - Additional PHPUnit arguments or options object
 * @returns CTRF report with test results
 */
export async function runPhpunitTests(
  config: ResolvedWPTesterConfig,
  phpunitArgsOrOptions?: string[] | RunPhpunitTestsOptions
): Promise<Report> {
  // Support both old signature (string[]) and new signature (options object)
  const options: RunPhpunitTestsOptions = Array.isArray(phpunitArgsOrOptions)
    ? { phpunitArgs: phpunitArgsOrOptions }
    : phpunitArgsOrOptions || {};
  const { phpunitArgs } = options;

  // Merge additional PHPUnit args if provided
  let resolvedConfig = { ...config };
  if (phpunitArgs && phpunitArgs.length > 0) {
    const configArgs = resolvedConfig.tests.phpunit?.phpunitArgs || [];
    const mergedArgs = [...configArgs, ...phpunitArgs];

    resolvedConfig = {
      ...resolvedConfig,
      tests: {
        ...resolvedConfig.tests,
        phpunit: resolvedConfig.tests.phpunit
          ? {
              ...resolvedConfig.tests.phpunit,
              phpunitArgs: mergedArgs,
            }
          : undefined,
      },
    };
  }

  // Check if PHPUnit tests are configured
  if (!shouldRunPhpunitTests(resolvedConfig)) {
    return Promise.resolve(EMPTY_REPORT);
  }

  // Get PHPUnit config path from resolved config (already absolute after resolveConfig)
  const hostPhpunitConfigPath = resolvedConfig.tests.phpunit!.configPath;

  // Verify the config file exists
  try {
    await access(hostPhpunitConfigPath);
  } catch {
    console.error(`PHPUnit config file not found: ${hostPhpunitConfigPath}`);
    return Promise.resolve(EMPTY_REPORT);
  }

  // Run tests for all enabled environments (skip those marked with skip: true)
  const reports: Report[] = [];
  const enabledEnvironments = resolvedConfig.environments.filter(
    (env) => !env.skip
  );
  for (const environment of enabledEnvironments) {
    const report = await runPhpunitTestsForEnvironment(
      resolvedConfig,
      environment,
      hostPhpunitConfigPath
    );
    // Include report only if it has tests
    if (report.results.summary.tests > 0) {
      reports.push(report);
    }
  }

  // Return empty report if no tests ran
  if (reports.length === 0) {
    return EMPTY_REPORT;
  }

  // Return single report as-is, or merge multiple reports
  if (reports.length === 1) {
    return reports[0];
  }

  // Import mergeReports for multiple environments
  return mergeReports(reports);
}

/**
 * PHPUnit flags that are boolean (do not take a value).
 * If a flag is NOT in this list, we assume it takes a value,
 * and therefore the next argument should NOT be resolved as a path.
 */
const booleanFlags = new Set([
  '--teamcity', '--debug', '--verbose', '--testdox',
  '--stderr', '--stop-on-error', '--stop-on-failure', '--stop-on-warning',
  '--stop-on-defect', '--stop-on-risky', '--stop-on-skipped', '--stop-on-incomplete',
  '--stop-on-deprecation', '--stop-on-notice', // Added in recent versions
  '--fail-on-warning', '--fail-on-risky', '--fail-on-incomplete', '--fail-on-skipped',
  '--fail-on-deprecation', '--fail-on-notice', // Added in recent versions
  '--strict-coverage', '--strict-global-state', '--disallow-test-output',
  '--disallow-resource-usage', '--enforce-time-limit',
  '--process-isolation', '--no-globals-backup', '--static-backup',
  '--no-configuration', '--no-coverage', '--no-logging', '--no-interaction',
  '--no-extensions', '--no-output', '--dont-report-useless-tests',
  '--no-progress', // Added
  '--display-deprecations', '--display-errors', '--display-incomplete', // Added
  '--display-notices', '--display-skipped', '--display-warnings', // Added
  '--strict',
  '--help', '--version',
  '--list-groups', '--list-suites', '--list-tests', '--list-tests-xml',
  // Colors often handled as boolean in simple usage
]);

// Minimal interface for Playground client
interface PlaygroundClient {
  fileExists(path: string): Promise<boolean>;
}

/**
 * Resolve PHPUnit arguments to map host paths to VFS paths
 *
 * @param args - Original arguments
 * @param config - Resolved configuration
 * @param playground - Playground client for checking VFS file existence
 * @returns Resolved arguments
 */
export async function resolvePhpunitArgs(
  args: string[],
  config: ResolvedWPTesterConfig,
  playground: PlaygroundClient
): Promise<string[]> {
  const resolvedArgs: string[] = [];

  for (let i = 0; i < args.length; i++) {
    const arg = args[i];

    // Don't resolve flags
    if (arg.startsWith('-')) {
      resolvedArgs.push(arg);
      continue;
    }

    // Check if the previous argument was a flag that takes a value
    const prevArg = args[i - 1];
    if (prevArg?.startsWith('-') && !prevArg.includes('=')) {
      // If the previous flag is NOT a known boolean flag, assume it takes a value
      const flagName = prevArg.split('=')[0];
      if (!booleanFlags.has(flagName)) {
        resolvedArgs.push(arg); // This is the flag's value, not a path
        continue;
      }
    }

    // Resolve arguments that look like file/directory paths
    const looksLikePath = arg.includes('/') ||
                          arg.endsWith('.php') ||
                          arg === 'tests' || arg === 'test';

    if (looksLikePath) {
      const absoluteHostPath = resolveAbsolute(arg, config.projectHostPath);
      const vfsPath = hostToVfs(absoluteHostPath, config);

      try {
        // Validate that the file actually exists in the VFS
        const exists = await playground.fileExists(vfsPath);
        if (exists) {
            resolvedArgs.push(vfsPath);
            continue;
        }
      } catch {
        // Ignore validation errors and fallback to original arg (or should we?)
        // If file check fails, it's safer to probably not resolve it if we are strict,
        // but existing behavior was "looksLikePath -> resolve".
        // If we add this check, we might break things that "look like path" but aren't files YET?
        // But for running tests, the files must exist.
      }
    }

    resolvedArgs.push(arg);
  }

  return resolvedArgs;
}

